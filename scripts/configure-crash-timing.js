"use strict";
const fs = require("fs");
const path = require("path");
const DEFAULT_BETTING_MS = 30_000;
const bettingMs = Number.parseInt(process.env.CRASH_BETTING_MS || String(DEFAULT_BETTING_MS), 10);
if (!Number.isInteger(bettingMs) || bettingMs < 5_000 || bettingMs > 120_000) throw new Error("CRASH_BETTING_MS must be between 5000 and 120000 milliseconds.");
const root = path.join(__dirname, "..");
const backendFile = path.join(root, "website", "lib", "crash-routes-contract.js");
const clientFile = path.join(root, "website", "public", "crash-mainnet.js");
const htmlFile = path.join(root, "website", "public", "crash.html");
const formatted = bettingMs.toLocaleString("en-US").replace(/,/g, "_");
for (const filename of [backendFile, clientFile, htmlFile]) if (!fs.existsSync(filename)) throw new Error(`Missing Crash file: ${filename}`);
let backend = fs.readFileSync(backendFile, "utf8").replace(/const BETTING_MS = [\d_]+;/, `const BETTING_MS = ${formatted};`);
let client = fs.readFileSync(clientFile, "utf8").replace(/const BETTING_MS = [\d_]+;/, `const BETTING_MS = ${formatted};`);
let html = fs.readFileSync(htmlFile, "utf8");
if (!backend.includes('router.post("/session/challenge"') || !client.includes("crashSessionToken") || client.includes("signer.signMessage(cashoutText")) throw new Error("Instant Crash session cash-out code is missing; refusing to start an unsafe build.");
const commit = String(process.env.RENDER_GIT_COMMIT || process.env.SOURCE_VERSION || Date.now()).slice(0, 12);
html = html.replace(/\/crash-mainnet\.js\?v=[^\"']+/g, `/crash-mainnet.js?v=instant-session-${bettingMs}-${commit}`);
fs.writeFileSync(backendFile, backend);
fs.writeFileSync(clientFile, client);
fs.writeFileSync(htmlFile, html);
console.log(`MATT Crash betting window: ${bettingMs / 1000}s. Instant session cash-out verified.`);
